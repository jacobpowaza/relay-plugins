#!/usr/bin/env node
import { cpSync, existsSync, mkdirSync, readFileSync, renameSync, writeFileSync } from "node:fs";
import { homedir } from "node:os";
import { dirname, join } from "node:path";
import { fileURLToPath } from "node:url";

const home = homedir();
const settingsPath = join(home, ".claude", "settings.json");
const hooksDir = join(home, ".claude", "hooks");
const scriptDir = dirname(fileURLToPath(import.meta.url));
const relayScript = join(scriptDir, "statusline.mjs");
const force = process.argv.includes("--force");

function readFileSafe(path) {
  try { return readFileSync(path, "utf8"); }
  catch { return undefined; }
}

function escSingle(str) {
  return "'" + str.replace(/'/g, "'\\''") + "'";
}

function writeFileAtomic(path, data) {
  const tmp = path + ".tmp." + Date.now();
  mkdirSync(dirname(path), { recursive: true });
  writeFileSync(tmp, data, { mode: 0o600 });
  renameSync(tmp, path);
}

let settingsRaw = readFileSafe(settingsPath);
if (settingsRaw === undefined) settingsRaw = "{}";

let settings;
try {
  settings = JSON.parse(settingsRaw);
} catch (err) {
  console.error(`ERROR: Could not parse ${settingsPath}.`);
  console.error(`  ${err.message}`);
  console.error("Fix the file or remove it, then re-run this script.");
  process.exit(1);
}

const existing = settings.statusLine ?? {};
const existingCmd = (existing.command ?? "").trim();
const containsRelay = existingCmd.includes("statusline.mjs");

if (!force && containsRelay) {
  console.log("Relay statusLine already registered. No changes needed.");
  process.exit(0);
}

let newCommand;

if (force || !existingCmd) {
  newCommand = `node ${escSingle(relayScript)}`;
  if (!existingCmd) {
    console.log("No existing statusLine found. Registering Relay.");
  } else {
    console.log("--force: overwriting existing statusLine.");
  }
} else {
  const wrapperPath = join(hooksDir, "relay-composed.sh");
  const wrapper = [
    "#!/usr/bin/env bash",
    "# Generated by Relay install-statusline.mjs",
    "# Combines existing status-line command with the Relay indicator.",
    "# Run install-statusline.mjs again to regenerate.",
    "#",
    "# Architecture: generic composition requires shell execution because the",
    "# existing statusLine.command is an arbitrary shell string from settings.json.",
    "# This wrapper treats settings.json as trusted user configuration and delegates",
    "# the existing command to bash -c, the same way Claude Code itself would.",
    "set -euo pipefail",
    `INPUT=$(cat /dev/stdin 2>/dev/null)`,
    `RELAY_OUTPUT=$(node ${escSingle(relayScript)} 2>/dev/null <<<"$INPUT") || true`,
    `EXISTING_OUTPUT=$(bash -c ${escSingle(existingCmd)} 2>/dev/null <<<"$INPUT") || true`,
    'echo "${RELAY_OUTPUT} ${EXISTING_OUTPUT}" | sed "s/  */ /g; s/^ *//; s/ *$//"',
    "",
  ].join("\n");
  mkdirSync(hooksDir, { recursive: true });
  writeFileSync(wrapperPath, wrapper, { mode: 0o755 });
  newCommand = `bash ${escSingle(wrapperPath)}`;
  console.log("Existing statusLine detected. Composed wrapper created:");
  console.log(`  ${wrapperPath}`);
}

settings.statusLine = {
  type: "command",
  command: newCommand,
  refreshInterval: 2,
  padding: 0,
};

const backupPath = settingsPath + ".bak." + Date.now();
if (settingsRaw !== "{}") {
  cpSync(settingsPath, backupPath);
  console.log(`Original settings backed up to:\n  ${backupPath}`);
}

writeFileAtomic(settingsPath, JSON.stringify(settings, null, 2) + "\n");
console.log(`Settings updated:\n  ${settingsPath}`);
console.log("Note: this modifies the GLOBAL user scope (~/.claude/settings.json).");
console.log("Project-local .claude/settings.json files are NOT changed.");
